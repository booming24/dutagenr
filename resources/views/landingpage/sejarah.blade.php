    <script>
    const carousel = document.querySelector(".carousel");
    const prevButton = document.getElementById("prev");
    const nextButton = document.getElementById("next");
    const carouselCards = document.querySelectorAll(".carousel-card");
    let currentIndex = 0;

    prevButton.addEventListener("click", () => {
        if (currentIndex > 0) {
            currentIndex--;
        } else {
            currentIndex = carouselCards.length - 1; // Kembali ke gambar terakhir jika sudah di awal
        }
        updateCarousel();
    });

    nextButton.addEventListener("click", () => {
        if (currentIndex < carouselCards.length - 1) {
            currentIndex++;
        } else {
            currentIndex = 0; // Kembali ke awal jika sudah di gambar terakhir
        }
        updateCarousel();
    });

    function updateCarousel() {
        const cardWidth = carouselCards[0].clientWidth;
        carousel.style.transform = `translateX(-${currentIndex * (cardWidth + 20)}px)`;
    }
    </script>
