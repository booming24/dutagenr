        <p>Program Genre (Generasi berencana) diinisiasi pada tahun 2007 oleh Bapak Dr. Sudibyo Alimoeso yang saat itu menjabat sebagai Deputi KSPK BKKBN. Program ini dilatarbelakangi oleh banyaknya kenakalan remaja yang terjadi. Seperti seksualitas, penyalahgunaan narkoba, seks bebas dan nikah muda. <br><br>

            Fenomena kenakalan remaja yang terjadi diiringi dengan jumlah remaja yang hampir 30% dari penduduk Indonesia. Hal ini tentunya menjadi fokus Badan Kependudukan dan Keluarga Berencana Nasional karena terkait dengan kependudukan. Terlebih, remaja yang merupakan generasi penerus bangsa, perlu dipersiapkan menjadi manusia yang sehat secara jasmani, rohani, mental dan spiritual.<br><br>

            Oleh karena itu diperlukan suatu program yang dapat memberikan informasi yang berkaiatan dengan penyiapan diri remaja menyongsong kehidupan berkeluarga yang lebih baik, menyiapakan pribadi yang matang dalam membangun keluarga yang harmonis, dan memantapkan perencanaan dalam menata kehidupan untuk keharmonisan keluarga. Sebagai Implementasi Undang-Undang Nomor 52 tahun 2009, tentang perkembangan kependudukan dan pembangunan keluarga, pasal 48 ayat 1 (b) yang mengatakan bahwa “peningkatan kualitas remaja dengan dengan pemberian akases informasi, pendidikan, konseling, dan pelayanan tentang kehidupan berkeluarga,” maka BKKBN sebagai salah satu istitusi pemerintah harus mewujudkan tercapainya peningkatan kualitas remaja melalui program Generasi Berencana.<br><br>

            Melalui program GenRe, BKKBN membuat Pusat Informasi Konseling Remaja dan Mahasiswa (PIK R/M) dan bekerja sama dengan para mahasiswa dengan mengadakan Pemilihan Duta Mahasiswa GenRe.<br><br>

            BKKBN Perwakilan Sumatera Selatan selaku pendukung jalannya program BKKBN pusat juga melaksanakan semua program kerja termasuk mengadakan Pemilihan Duta Mahasiswa GenRe di wilayah provinsi Sumatera Selatan.</p>
            <b>Pemilihan Duta Genre Sumsel 2010-2018</b>
            <p>Pada tahun 2010 BKKBN perwakilan Sumatera Selatan mengirimkan utusan Duta Mahasiswa GenRe dari Universitas Sriwijaya ke tingkat nasional, atas nama Hadi dan Maretha Wulandari.<br><br>

Selanjutnya pada tahun 2011, pemilihan Duta Mahasiswa GenRe Sumatera Selatan diadakan di BKKBN Perwakilan Provinsi Sumatera Selatan dengan terpilihnya Aby Salam dan Diah Anggi beserta pemenang juara 2, 3, dan favorit.<br><br>

Di tahun 2012, pemilihan Duta Mahasiswa GenRe Sumatera Selatan kembali diadakan di BKKBN Perwakilan Sumatera Selatan dengan terpilihnya Andre Wilyanto dan Yessi Ismiralda beserta juara 2,3, dan favorit serta berhasil masuk Top 10 Duta Mahasiswa.<br><br>

Di tahun 2013 diadakan di hotel azza. Duta Mahasiswa GenRe Sumatera Selatan kembali menorehkan prestasi di tingkat nasional, yaitu meraih Juara Fotogenik Putri Duta Mahasiswa GenRe, atas nama Nova Nur Asih.<br><br>

Pada tahun 2014 pemilihan Duta Mahasiswa GenRe diadakan di Hotel Emilia dengan terpilihnya 10 pasang pemenang yang terdiri dari juara 1,2,3, harapan 1, harapan 2 , harapan 3, persahabatan, favorit, berbakat dan fotogenik. Ariadi dan Indah Dian Permata  mewakili Duta Mahasiswa GenRe Sumsel ke tingkat nasional.
<br><br>
Tahun 2015  kembali diadakan  pemilihan Duta Mahasiswa GenRe yang dilaksanakan di Atyasa Convention Centre dengan terpilihnya 10 pasang pemenang. Aldy Pajero dan Rika Damayanti berhasil meraih gelar Duta Mahasiswa GenRe 2015.<br><br>

Di tahun 2016 pemilihan Duta Mahasiswa GenRe diadakan di hotel horison ultima dengan terpilihnya 10 pasang pemenang. Duta Mahasiswa GenRe Sumatera Selatan kembali mengharumkan nama provinsi dengan meraih gelar Motivator Putri Duta GenRe Tingkat Nasional atas nama Tania Dwi Adinda.<br><br>

BKKBN perwakilan Sumatera Selatan mengadakan pemilihan Duta Mahasiswa Genre. Pada tahun 2017 atas keputusan dari BKKBN pusat, konsep pemilihan Duta Mahasiswa GenRe terjadi perubahan. Pemilihan Duta Mahasiswa GenRe menjadi Duta GenRe Sumatera Selatan lalu diseleksi melalui dua jalur yaitu, jalur pendidikan dan jalur masyarakat. Sehingga terpilihlah 9 pasang pemenang yang terdiri dari juara 1, 2, dan 3 jalur pendidikan, juara 1,2, dan 3 jalur masyarakat serta pemenang atribut berupa berbakat, favorit, dan persahabatan. Di tahun 2017 pula nama Sumsel semakin meroket karena berhasil meraih gelar Juara 1 Putra Duta GenRe tingkat nasional atas nama Dion Patria Nugraha dan Adella Tinesa, Duta GenRe Putri jalur masyarakat yang juga berhasil masuk dalam top 10.
